package com.pfm.auth;

import io.swagger.annotations.ApiModelProperty;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;
import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;

@Entity
@Builder
@Data
@AllArgsConstructor
@NoArgsConstructor
@Table(name = "app_user")
public final class User {

  @Id
  @GeneratedValue(strategy = GenerationType.IDENTITY)
  @ApiModelProperty(value = "User id (generated by application)", required = true, example = "1")
  private Long id;

  @ApiModelProperty(value = "User name", required = true, example = "Marian")
  private String username;

  @ApiModelProperty(value = "User password", required = true, example = "123456789Marian")
  private String password;

  @ApiModelProperty(value = "User first name", required = true, example = "Marian")
  private String firstName;

  @ApiModelProperty(value = "User last name", required = true, example = "Pazdzioch")
  private String lastName;

}
